    void autenticar() throws SQLException {

        MetodosAplicacao metodosAplicacao = new MetodosAplicacao();
        PessoaDAO pessoaDAO = new PessoaDAO();
        boolean condicao;
        String senha = campoSenha.getText();
        String cpf = campoLogin.getText();
        condicao = metodosAplicacao.AutenticarUsuario(senha, cpf);
        
        if(caixaSelecaoSenha.isSelected() && condicao){
            Pessoa pessoa = new Pessoa();
            pessoa = pessoaDAO.pesquisarPessoa(cpf);
            principal.exibirTelaNovaSenha();
            principal.getTelaNovaSenha().setPessoa(pessoa);
            this.dispose();
        }else{
             if (condicao == true) {
            this.dispose();
            principal.ativarBotao();
        }
            
        }
        
       

    }

